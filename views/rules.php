      <a name="rules1"></a>
      <h1>1. EQUIPMENT</h1>
      <p>The eqipment required to play regulation BinBall.</p>
      <hr class="mb20 mt20" />
      
      <a name="rules1.1"></a>
      <h2>1.1. Bin</h2>
      <p>The bin is one of the most important items, it is <b>required</b> to play BinBall.</p>

      <ul class="rules-list alert alert-info">
        <li>A regulation BinBall bin is between 1' (12") and 1.5' (18") in height and diameter</li>
        <li>The bin should be made of a solid material (e.g. plastic) to facilitate bouncing</li>
        <li>The BinBall bin must be placed so that the base of the bin is between 1' (12") and 1.5' (18") from a single wall</li>
      </ul>
      <hr class="mb20 mt20" />
      
      <a name="rules1.2"></a>
      <h2>1.2. Ball</h2>
      <p>The ball is one of the most important items, it is <b>required</b> to play BinBall.</p>

      <ul class="rules-list alert alert-info">
        <li>A regulation BinBall ball is approximately 3" in diameter</li>
        <li>A regulation BinBall is made of a solid, but spongey material and should bounce easily</li>
      </ul>
      <hr class="mb20 mt20" />
      
      <a name="rules1.3"></a>
      <h2>1.3. Measures</h2>
      <p>Although a measure is not a physical piece of equipment it is important to understand what a measure is and how it is applied to BinBall.</p>
      <p>A measure is a fixed distance, determined pre-game, that is used to mark out the distances the players will throw from.</p>
      <p>A measure is typically 18" in length, however it is suggested that measure sizes be adapted to suit the playing environment (i.e. the length of a floor tile) for ease and consistency</p>

      <hr class="mb20 mt20" />
      
      <a name="rules2"></a>
      <h1>2. PLAYING BINBALL</h1>
      <p>Everything you need to know to get started with BinBall.</p>
      <hr class="mb20 mt20" />
      
      <a name="rules2.1"></a>
      <h2>2.1. Player positioning</h2>
      <p>For the first throw, the player must be stood a minimum of three measures from the front of the bin.</p>
      <p>After each round, the players will move back one additional measure up to the maximum number of measures (agreed pre-game).</p>

      <ul class="rules-list alert alert-info">
        <li>A measure is typically 18" in length (or, the length of one floor tile)</li>
        <li>This can be adapted to suit the playing environment</li>
      </ul>
      <hr class="mb20 mt20" />
      
      <a name="rules2.2"></a>
      <h2>2.2. Throwing the ball</h2>
      <p>The current player must attempt to score points by throwing the ball into the bin. Any method of throwing is applicable, including overarm, underarm, bouncing or even kicking.</p>
      <p>The players must remain the required number of measures away from the front of the bin at all times, or that throw is deemed a 'no-throw' and classed as a failed attempt.</p>

      <ul class="rules-list alert alert-info">
        <li>Only a players feet must remain behind the required distance away, leaning forward is allowed</li>
      </ul>
      <hr class="mb20 mt20" />
      
      <a name="rules2.3"></a>
      <h2>2.3. Scoring points</h2>
      <p>The current player gets three attempts to get the ball into the bin.</p>
      <p>If the player is successful in getting the ball into the bin, he/she will receive some points. The number of points awarded is equal to the number of measures away from the bin, minus the number of failed attempts.</p>
      <p>The ball must stay in the bin to be classed as a successful throw. If the ball bounces out is is classed as a failed attempt and no points are awarded.</p>
      <p>If the ball ends up in the bin after bouncing off a surface other than the floor (e.g. a wall, or a nearby desk), this is classed as a <b>valid</b> throw and the usual points are awarded.</p>
      <p>A player can nominate the current throw as a <b>Joker</b>. If a nominated throw is successful, then the player will receive double the amount of points for that throw. A player only has 1 Joker to use through the whole game.</p>
      

      <ul class="rules-list alert alert-info">
        <li>A player is throwing from a distance of 7 measures and misses his first 2 attempts. His 3rd attempt is successful. He will be awarded 5 points</li>
        <li>If he had played his joker on this throw, he would have received 10 points</li>
      </ul>

